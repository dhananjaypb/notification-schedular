package org.notification.schedular.model;

public class Clouds {
	
	private String all;

	public Clouds() {
		super();
	}

	public Clouds(String all) {
		super();
		this.all = all;
	}

	public String getAll() {
		return all;
	}

	public void setAll(String all) {
		this.all = all;
	}

	@Override
	public String toString() {
		return "Clouds [all=" + all + "]";
	}
	
	

}
